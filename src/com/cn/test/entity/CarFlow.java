package com.cn.test.entity;

public class CarFlow {
	private long id;
	private int stationId;
	private String userName;
	private int EnTotalVehiNum;
	private int ExTotalVehiNum;
	private String date;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public int getStationId() {
		return stationId;
	}
	public void setStationId(int stationId) {
		this.stationId = stationId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public int getEnTotalVehiNum() {
		return EnTotalVehiNum;
	}
	public void setEnTotalVehiNum(int enTotalVehiNum) {
		EnTotalVehiNum = enTotalVehiNum;
	}
	public int getExTotalVehiNum() {
		return ExTotalVehiNum;
	}
	public void setExTotalVehiNum(int exTotalVehiNum) {
		ExTotalVehiNum = exTotalVehiNum;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
